let numeros2 = [10, 20, 30, 40, 50];

numeros2.push(125);
console.log(numeros2);

//pop() --> elimina el último elemento del array
numeros2.pop();
console.log(numeros2);

//unshif() --> agrega un elemento al inicio
numeros2.unshift(100);
console.log(numeros2);

//shif() --> elimina el primero
numeros2.shift();
console.log(numeros2);